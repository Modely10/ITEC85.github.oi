          <p>Incident Response and Forensics are two key components of cybersecurity aimed at handling and investigating security incidents.</p>

           <br>
               <p>No 1. Incident response refers to the process of addressing and managing a security incident within an organization. It involves a structured approach to detecting, analyzing, and responding to security breaches, cyberattacks, or other unauthorized activities. The primary goals of incident response are to minimize the impact of the incident, restore normal operations, and prevent similar incidents from occurring in the future.</p>
               <br>
                <p>No 2. Digital forensics, also known as computer forensics or cyber forensics, is the process of collecting, analyzing, and preserving digital evidence to investigate and determine the cause of a security incident or cybercrime. It involves the application of scientific and investigative techniques to recover and analyze data from various digital sources, such as computers, servers, mobile devices, or network logs.</p>
